   <h1>Article 1</h1>
  <p style="text-indent:2em"> Une équipe est composée de 6 participants (garçon et/ ou filles).
   <h1>Article 2</h1>
   <p style="text-indent:2em"> Si un joueur est désisté (blessure ou autre) entre l'inscription et le jour des 
   Olympiades, et que son équipe arrive en infériorité numérique, l'équipe devra remplacer ce joueur afin de pouvoir participer.
   Sinon, dans la mesure du possible, les équipes en infériorité numerique seront reformés par nos soins, pour arriver au nombre de 
   6 participants par nos soins le jour des Olympiades. 
   <h1>Article 3</h1>
  <p style="text-indent:2em">  Pour éviter l'inégalité filles/garçons et inciter à la création d'équipes mixtes, chaque fille participant aux Olympiades se voit 
   attribuer 10 points d'avance.
   <h1>Article 4</h1>
   <p style="text-indent:2em"> Chaque équipe doit choisir de participer à un des 2 sports collectifs (foot ou basket) et participera à 5 activités annexes.
   <h1>Article 5</h1>
   <p style="text-indent:2em"> Pour les activités principales, football et basket, les tournois sont organisé par poule.Le vainqueur de chaque tournoi sera l'équipe ayant 
   gagné le plus de matchs et par conséquent, marqué le plus de points.
   <p style="text-indent:2em"> Si plusieurs équipes ont gagné le même nombre de matchs, l'équipe qui aura marqué le plus de buts terminera
   	première.
   	<p style="text-indent:2em"> Si elles ont marqué le même nombre de buts, la différence de buts sera appliquée (nombre de buts marqués - nombre de buts encaissés) .
   	<p style="text-indent:2em">  Si la différence de buts est égale, l'équipe la plus jeune finira première (moyenne d'âge la plus faible).
   	<h1>Article 6</h1>
   	<p style="text-indent:2em"> Chaque équipe devra suivre sa feuille de route, qui déterminera ses horaires d'épreuves dans les activités
   		secondaires et le tournoi de football ou de basket.
   	<h1>Article 7</h1>
   	<p style="text-indent:2em"> Validation des points par poinçonnage : chaque activvité une fois remportée est validée via un poinçon
   		sur une feuille.
   	<h1>Article 8</h1>
   	<p style="text-indent:2em"> Si une équipe participe à une activité anexe et qu'elle ne remporte pas de points, elle se voit attribuer un poinçon
   		spécial qui prouve sa participation mais qui vaut 0 points. 
   	<h1>Article 9</h1>
   	<p style="text-indent:2em"> L'alcool et toute substance illicite sont formellement interdites durnat cet évènement.
   	<h1>Article 10</h1>
   	<p style="text-indent:2em"> Les organisateurs déclinent toute responsabilité en cas de perte ou de vol d'effet personnel.
   	<h1>Article 11</h1>
   	<p style="text-indent:2em"> Les organisateurs déclinent toute responsabilité en cas de blessure d'un participant.
